# Использование двойных кавычек внутри строки, заключенной в двойные кавычки
double_quote_example = "Они пропели: \"Вместе весело шагать по просторам\""
print(double_quote_example)  # Вывод: Они пропели: "Вместе весело шагать по просторам"


# Использование одинарных кавычек внутри строки, заключенной в одинарные кавычки
single_quote_example = 'I\'m a beautiful girl!'
print(single_quote_example)  # Вывод: I'm a beautiful girl!


